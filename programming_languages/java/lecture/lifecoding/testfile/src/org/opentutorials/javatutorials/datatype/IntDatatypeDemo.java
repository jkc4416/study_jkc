package org.opentutorials.javatutorials.datatype;

public class IntDatatypeDemo {
    public static void main(String[] args){
        short a = 128;
        byte b;
        int c;
        long d;

        b = 127;
        c = 1251323551;
        d = 1232351235;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
